const sleep = (
  count: number,
  step: number = 100,
  ms: number = 5000
): Promise<unknown> | void => {
  if (count % step === 0 && count !== 0) {
    return new Promise((r) => setTimeout(r, ms));
  }
};

const message = (
  type: "warning" | "error" | "info" | "success",
  message: string,
  description: string | undefined
): void => {
  notification[type]({
    message: message,
    description: description,
    duration: 5,
    placement: "bottomRight",
  });
};

const convertDecimalsToUnit = (decimals: string) => {
  switch (decimals) {
    case "0":
      return "wei";
    case "3":
      return "kwei";
    case "6":
      return "mwei";
    case "9":
      return "gwei";
    case "12":
      return "microether";
    case "15":
      return "milliether";
    case "18":
      return "ether";
  }
};

